package com.example.tureclore;

import java.util.ArrayList;

public class ColetivoData {

	private String lineaColectivo;
	private String cartel;
	
	public ColetivoData(String lineaColectivo, String cartel) {
		super();
		this.lineaColectivo = lineaColectivo;
		this.cartel = cartel;
	}

	public String getLineaColectivo() {
		return lineaColectivo;
	}

	public void setLineaColectivo(String lineaColectivo) {
		this.lineaColectivo = lineaColectivo;
	}

	public String getCartel() {
		return cartel;
	}

	public void setCartel(String cartel) {
		this.cartel = cartel;
	}

	
	
	
}
